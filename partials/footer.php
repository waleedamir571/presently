<script>
    document.addEventListener('DOMContentLoaded', function () {
        const addCollectionBtn = document.getElementById('addCollectionBtn');

        // Function to check active tab and show/hide the button
        function handleTabChange() {
            const activeTab = document.querySelector('.nav-link.active');
            if (activeTab && activeTab.id === 'collections-tab') {
                addCollectionBtn.style.display = 'inline-block'; // show
            } else {
                addCollectionBtn.style.display = 'none'; // hide
            }
        }

        // Initial check on page load
        handleTabChange();

        // Listen for tab switch
        const navLinks = document.querySelectorAll('.nav-link');
        navLinks.forEach(link => {
            link.addEventListener('shown.bs.tab', handleTabChange);
        });
    });
</script>



<script>
    const trigger = document.getElementById("trigger");
    const dropdown = document.getElementById("dropdown");

    trigger.addEventListener("click", () => {
        dropdown.style.display = dropdown.style.display === "block" ? "none" : "block";
    });

    // Hide dropdown if clicked outside
    window.addEventListener("click", (e) => {
        if (!trigger.contains(e.target) && !dropdown.contains(e.target)) {
            dropdown.style.display = "none";
        }
    });
</script>

